a = input('введите логин: ')
for i in a:
    if i == '=':
        print('=')
    if i == '?':
        print('?')
    if i == '*':
        print('*')
    if i == '$':
        print('$')
    if i == '№':
        print('№')
    if i == '@':
        print('@')
    if i == '_':
        print('-')